package vn.edu.tlu.tl2151173791;

public class Cafe {
    private String name;
    private int price;

    public Cafe(String name, int price) {
        this.name = name;
        this.price = price;
    }
    public String getName() {
        return name;
    }
    public int getPrice() {
        return price;
    }
}
